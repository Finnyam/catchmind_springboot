package com.springboot.catchmind.vo;

public class TabletypeVo {
	String sid;
	int rooftop, terrace, bar, dininghall, windowseat, privateroom, rental;
	
	
	
	public String getSid() {
		return sid;
	}
	public void setSid(String sid) {
		this.sid = sid;
	}
	public int getRooftop() {
		return rooftop;
	}
	public void setRooftop(int rooftop) {
		this.rooftop = rooftop;
	}
	public int getTerrace() {
		return terrace;
	}
	public void setTerrace(int terrace) {
		this.terrace = terrace;
	}
	public int getBar() {
		return bar;
	}
	public void setBar(int bar) {
		this.bar = bar;
	}
	public int getDininghall() {
		return dininghall;
	}
	public void setDininghall(int dininghall) {
		this.dininghall = dininghall;
	}
	public int getWindowseat() {
		return windowseat;
	}
	public void setWindowseat(int windowseat) {
		this.windowseat = windowseat;
	}
	public int getPrivateroom() {
		return privateroom;
	}
	public void setPrivateroom(int privateroom) {
		this.privateroom = privateroom;
	}
	public int getRental() {
		return rental;
	}
	public void setRental(int rental) {
		this.rental = rental;
	}
}
